#include <stdio.h>

#define UART_TX_BUF_SIZE 1024U

typedef struct
{
  uint8_t tx_buf[UART_TX_BUF_SIZE];
  volatile uint16_t tx_head;
  volatile uint16_t tx_tail;
  volatile uint8_t tx_busy;
  uint8_t tx_byte;
} UartTxState_t;

static UartTxState_t g_uart1_tx = {0};

static void uart_tx_start_it(UART_HandleTypeDef *huart, UartTxState_t *state)
{
  if ((state == 0) || (state->tx_busy != 0U) || (state->tx_head == state->tx_tail))
  {
    return;
  }

  state->tx_busy = 1U;
  state->tx_byte = state->tx_buf[state->tx_tail];
  state->tx_tail = (uint16_t)((state->tx_tail + 1U) % UART_TX_BUF_SIZE);

  if (HAL_UART_Transmit_IT(huart, &state->tx_byte, 1U) != HAL_OK)
  {
    state->tx_busy = 0U;
  }
}

static void uart_tx_queue_byte(UART_HandleTypeDef *huart, UartTxState_t *state, uint8_t byte)
{
  uint16_t next;

  if (state == 0)
  {
    return;
  }

  for (;;)
  {
    while (((state->tx_head + 1U) % UART_TX_BUF_SIZE) == state->tx_tail)
    {
    }

    __disable_irq();
    next = (uint16_t)((state->tx_head + 1U) % UART_TX_BUF_SIZE);
    if (next != state->tx_tail)
    {
      state->tx_buf[state->tx_head] = byte;
      state->tx_head = next;
      __enable_irq();
      break;
    }
    __enable_irq();
  }

  uart_tx_start_it(huart, state);
}

int fputc(int ch, FILE *f)
{
  (void)f;
  uart_tx_queue_byte(&huart1, &g_uart1_tx, (uint8_t)ch);
  return ch;
}

void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart)
{
  UartTxState_t *state;

  if (huart->Instance != USART1)
  {
    return;
  }

  state = &g_uart1_tx;

  if (state->tx_head == state->tx_tail)
  {
    state->tx_busy = 0U;
    return;
  }

  state->tx_byte = state->tx_buf[state->tx_tail];
  state->tx_tail = (uint16_t)((state->tx_tail + 1U) % UART_TX_BUF_SIZE);

  if (HAL_UART_Transmit_IT(huart, &state->tx_byte, 1U) != HAL_OK)
  {
    state->tx_busy = 0U;
  }
}

